/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */

import java.util.ArrayList;

public class T109 {
    public TreeNode dfs(ArrayList<Integer> arr, int left, int righ) {
        if (left > righ) return null;

        int mid = (left + righ) / 2;
        TreeNode node = new TreeNode(arr.get(mid));
        node.left = dfs(arr, left, mid - 1);
        node.right = dfs(arr, mid + 1, righ);

        return node;
    }

    public TreeNode sortedListToBST(ListNode head) {
        ArrayList<Integer> arr = new ArrayList<>();
        ListNode node = head;

        while (head != null) {
            arr.add(head.val);
            head = head.next;
        }

        return dfs(arr, 0, arr.size() - 1);
    }

    public static void main(String[] args) {
        ListNode[] nodes = new ListNode[5];
        nodes[0] = new ListNode(-10);
        nodes[1] = new ListNode(-3);
        nodes[2] = new ListNode(0);
        nodes[3] = new ListNode(5);
        nodes[4] = new ListNode(9);

        for (int i = 0; i < nodes.length - 1; i++) {
            nodes[i].next = nodes[i + 1];
        }

        TreeNode root = new T109().sortedListToBST(nodes[0]);
        root.printTree();
    }
}
